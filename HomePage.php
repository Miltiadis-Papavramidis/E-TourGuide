                <h3>Greek Islands</h3>
                <p>
According to the World Tourism Organization, Greece ranks 9th worldwide in terms of international tourist arrivals for 2022. According to the Border Survey of the Bank of Greece, the country receives at least 15 million tourists from abroad every year from 2015 onwards, with the only exception of 2020 due to the pandemic of Covid-19.

Most tourists come from Europe, especially the EU countries, and from the USA, while fewer originate from the other continents.

 
Why Greece?<br>
Greece is a famous country, popular for its clean beaches and rich history.<br>
Hundreds of archaeological and historical sites gloriously depict the country's past - besides, there is a reason why Athens is called an open-air museum!<br>
The landscape is mainly mountainous and the terrain is very fertile in the valleys that are scattered along the Greek mainland.<br>
Most of the mainland is also surrounded by water - by the Aegean and the Ionian Sea in particular.<br>
The country features around 6000 islands and islets, but only 227 of them are inhabited. They are the most popular Greek destinations, especially in summer. Big or tiny, green or arid, ideal for cosmopolitan or relaxing vacations, one thing is for sure: all have the requirements and facilities to offer you memorable and safe vacations!<br><br>

Some reputed island resorts are:<br>
• Mykonos, with its cosmopolitan character<br>
• Santorini, with the most romantic sunset in the world<br>
• Rhodes, the island of the knights, with its medieval castles and butterflies<br>
• Crete, with its revolutionary spirit<br>
• Ios, with its wild nightlife<br>
• Corfu, with its Venetian style<br>
• Ithaca, the homeland of Odysseus<br>
• Skiathos, with its long and sandy beaches<br>
All these constitute just a few examples of the most famous Greek paradises.<br>
Many have airports and are easily accessible via a flight from Athens or directly from other European cities. All of them are also accessible via ferry.<br><br>

